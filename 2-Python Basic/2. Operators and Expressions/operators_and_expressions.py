# Arithmetic Operators:

# Addition
addition_result = 10 + 5

# Subtraction
subtraction_result = 15 - 7

# Multiplication
multiplication_result = 3 * 4

# Division
division_result = 20 / 5

# Modulus
modulus_result = 10 % 3

# Exponentiation
exponentiation_result = 2 ** 3

# Comparison Operators:

# Equal to
is_equal = 5 == 5

# Not equal to
not_equal = 7 != 3

# Greater than
greater_than = 10 > 8

# Less than
less_than = 4 < 6

# Greater than or equal to
greater_than_or_equal = 20 >= 20

# Less than or equal to
less_than_or_equal = 15 <= 18

# Logical Operators:

# AND
logical_and_result = True and False

# OR
logical_or_result = True or False

# NOT
logical_not_result = not True

# Assignment Operators:

# Assignment
counter = 0

# Add and assign
counter += 1

# Subtract and assign
counter -= 1

# Multiply and assign
counter *= 2

# Divide and assign
counter /= 3

# Bitwise Operators:

# AND
bitwise_and_result = 0b1010 & 0b1100

# OR
bitwise_or_result = 0b1010 | 0b1100

# XOR
bitwise_xor_result = 0b1010 ^ 0b1100

# Left shift
left_shift_result = 0b1010 << 2

# Right shift
right_shift_result = 0b1010 >> 1

# Common Errors and Pitfalls:

# Misunderstanding Operator Precedence:
# Error: result = 2 + 3 * 4 might be misunderstood as (2 + 3) * 4 due to not following operator precedence.
# Solution: Encourage the use of parentheses to clarify the order of operations when needed.

# Mixing Data Types Incorrectly:
# Error: Attempting operations between incompatible data types.
# Solution: Emphasize the importance of using compatible data types or explicitly converting types.

# Forgetting the Difference Between = and ==:
# Error: Confusing assignment (=) with the equality comparison (==).
# Solution: Reinforce the distinction between assignment and comparison.

# Unintended String Concatenation:
# Error: Concatenating strings unintentionally.
# Solution: Remind learners to use proper syntax for string concatenation.

# Ignoring Type Conversion:
# Error: Not converting between data types when required.
# Solution: Teach the importance of type conversion and guide learners on when and how to perform it.

